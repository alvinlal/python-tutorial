x = int(3.14)
print(x)  # Output: 3

x = float(3)
print(x)  # Output: 3.0

x = str(3.14)
print(x)  # Output: 3.14

x = bool(0)
print(x)  # Output: false

x = list((1, 2, 3))
print(x)  # Output: [1,2,3]

x = set([1, 1, 2])
print(x)  # Output: {1,2}

x = tuple([1, 2, 3])
print(x)  # Output: (1, 2, 3)
